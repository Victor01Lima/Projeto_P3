    private void acessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_acessarActionPerformed
     String Text_BemVindo_NomeDoFuncionario="Bem Vindo ";
     String Usuario = usuarioText.getText();
     String senha = senhaText.getText();
        Controladora_Funcionario F = new Controladora_Funcionario();
     
     // teste para acessar com o funcionario ou ADM
     if(Usuario.equals("Admin") && senha.equals("admin")){
         TelaADM TL= new TelaADM();
         TL.setVisible(true);
         dispose();
     }
     else{
        boolean next= true;
        boolean acessoFuncionario=false;
         for(Funcionario e :F.listarFuncionarios()){
             String aux= e.getCPF_Funcionario().replace("-","");
             aux  = aux.replace(".","");
             if(Usuario.equals(e.getNome_Funcionario()) && senha.equals(aux) ){
                 acessoFuncionario=true;
                 Text_BemVindo_NomeDoFuncionario +=e.getNome_Funcionario();
                 break;
             }
         }
         if(acessoFuncionario){
             Tela_Funcionario TF = new Tela_Funcionario(Text_BemVindo_NomeDoFuncionario,"");
             TF.setVisible(true);
             dispose();
             
         }else{
             JOptionPane.showMessageDialog(null,"Usuario ou senha incorretos!");
         }
     
     }
     
    }//GEN-LAST:event_acessarActionPerformed

